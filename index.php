<p> The SYCL registry contains specifications of SYCL, a C++ programming
    model for <a href="https://www.khronos.org/registry/cl/"> OpenCL
    </a>. </p>

<h6> SYCL Specification </h6>

<p> The current version of SYCL is SYCL 1.2.1. Other versions are also linked. </p>

<ul>
<li> <a href="specs/sycl-1.2.1.pdf"> SYCL 1.2.1 Specification revision 5</a>
     (Apr 18, 2019). </li>
<li> <a href="specs/sycl-1.2.pdf"> SYCL 1.2 Specification </a> (May 8,
     2015). </li>
<li> A provisional SYCL 2.2 specification was published in February 2016.  That
     specification was an incomplete work in progress, and should be considered
     deprecated (it is no longer linked here).  The SYCL 1.2.1 specification is
     the latest version. </li>
</ul>

<h6> Providing Feedback on the Registry </h6>

<p> Khronos welcomes comments and bug reports. To provide feedback on the
    SYCL registry itself (such as reporting missing content, bad links,
    etc.), file an issue in the <a
    href="https://github.com/KhronosGroup/SYCL-Registry/issues">
    SYCL-Registry </a> Github project. </p>

<p> For the SYCL specification, file a bug on the <a
    href="http://www.khronos.org/bugzilla/"> Khronos Bugzilla </a>. Make
    sure to fill in the &quot;Product&quot; field in the bug entry form as
    &quot;OpenCL&quot; and the &quot;Component&quot; field as
    &quot;SYCL&quot;. </p>

